class Operations:
    """This is the default result property"""
    result = 0

    @staticmethod
    def add(v1, v2):
        """This is the add method"""
        return v1 + v2

    @staticmethod
    def subtract(v1, v2):
        """This is the subtract method"""
        return v1 - v2

    @staticmethod
    def multiply(v1, v2):
        """This is the add method"""
        return v1 * v2